x = 0;
y = 0;
a = input("Введите направление: ")
a = a.split()
if (a[0] == 'Влево'):
    x -= int(a[1]);
    print("Ваши координаты:({},{})".format(x,y))
elif (a[0] == 'Вправо'):
    x += int(a[1]);
    print("Ваши координаты:({},{})".format(x,y))
elif (a[0] == 'Вверх'):
    y += int(a[1]);
    print("Ваши координаты:({},{})".format(x,y))
elif (a[0] == 'Вниз'):
    y -= int(a[1]);
    print("Ваши координаты:({},{})".format(x,y))
else:
    print('Ошибка! Неправильный формат ввода данных.')